using UnityEngine;
using System.Text;

public class FlagGeneration : MonoBehaviour
{
    private const byte KEY = 0x5A;

    // Paste the bytes Unity printed here
    private readonly byte[] encryptedFlag = new byte[]
    {
        0x21, 0x0F, 0x37, 0x37, 0x05, 0x6E, 0x39, 0x2E, 0x2F, 0x3B, 0x36, 0x36, 0x23, 0x05, 0x2F, 0x34, 0x22, 0x6A, 0x28, 0x6B, 0x6B, 0x34, 0x3D, 0x05, 0x2E, 0x6A, 0x05, 0x3D, 0x69, 0x2E, 0x05, 0x3C, 0x36, 0x6E, 0x3D, 0x05, 0x29, 0x69, 0x69, 0x37, 0x29, 0x05, 0x2E, 0x6A, 0x6A, 0x05, 0x69, 0x6E, 0x29, 0x23, 0x27, 

    };

    string decryptFlag()
    {
        byte[] decrypted = new byte[encryptedFlag.Length];
        for (int i = 0; i < encryptedFlag.Length; i++)
            decrypted[i] = (byte)(encryptedFlag[i] ^ KEY);

        return Encoding.ASCII.GetString(decrypted);
    }
}

